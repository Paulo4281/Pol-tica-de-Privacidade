// SCRIPT PARA OS MEIOS DE CONTATO //

function ulMeiosDeContato(ul, formasDeContato, contatos) {
    for (let i = 0; i < formasDeContato.length; i++) {
        contatos[i] == undefined ? contatos[i] = 'A defenir...' : contatos[i];
        let li = $('<li>').attr('id', i).html(`${formasDeContato[i]}:&nbsp;` + `${contatos[i]}`);
        ul.append(li);
    }
}

