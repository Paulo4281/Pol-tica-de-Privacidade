// SCRIPT PARA COLOCAR AS <li> NAS <ul> //

function preencherDados(ul, dados=['']) {


    for (let i = 0; i < dados.length; i++) {
        let li = $('<li>').attr('id', i).html(dados[i]);
        ul.append(li);
    }

    
}

